    private void LoadData(){
        try{
            client = Gestion.CargarDatos();
        }
        catch(Exception ex)
        {
            Error.setText(ex.getMessage());
        }
        ln = client.size();
        k= new Object[ln][6];
        for(int i =0;i<ln;i++){
            k[i][0] = client.get(i).getNombre();
            k[i][1] = client.get(i).getApellidos();
            k[i][2] = client.get(i).getCarnetIdentidad();
            k[i][4] = client.get(i).getEdad();
            k[i][5] = false;
        }
        jTable2.setModel(new javax.swing.table.DefaultTableModel(
            k,
            new String [] {
                "Nombre", "Apellido", "CI", "Edad", "Delete"
            }
        ) {
            Class[] types = new Class [] {
                java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.Integer.class, java.lang.Boolean.class
            };
            boolean[] canEdit = new boolean [] {
                true, true, false, true, true, true
            };

            public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        });
        jScrollPane2.setViewportView(jTable2);
        if (jTable2.getColumnModel().getColumnCount() > 0) {
            jTable2.getColumnModel().getColumn(0).setResizable(false);
            jTable2.getColumnModel().getColumn(1).setResizable(false);
            jTable2.getColumnModel().getColumn(2).setResizable(false);
            jTable2.getColumnModel().getColumn(3).setResizable(false);
            jTable2.getColumnModel().getColumn(4).setResizable(false);
            jTable2.getColumnModel().getColumn(5).setResizable(false);
        }


    }
