    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        client = new Vector();
        for(int i =0;i<ln;i++){
            String nombre = (String)jTable2.getModel().getValueAt(i, 0);
            String apellido = (String)jTable2.getModel().getValueAt(i, 1);
            String ci = (String)jTable2.getModel().getValueAt(i, 2);
            String direc = (String)jTable2.getModel().getValueAt(i, 3);
            int edad = (int)jTable2.getModel().getValueAt(i, 4);
            Estudiantes info = new Estudiantes(nombre, apellido, ci, edad);
            client.add(info);
        }
        try{
        Gestion.GuardarDatos(client);
        }
        catch(Exception ex){
            Error.setText(ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        for(int i =0;i<ln;i++){
            if((boolean)jTable2.getModel().getValueAt(i, 5)==true){
                client = Gestion.Delete_Client((String) k[i][2],client);
            }
        }
        try{
        Gestion.GuardarDatos(client);
        }
        catch(Exception ex){
            Error.setText(ex.getMessage());
        }
        LoadData();
    }//GEN-LAST:event_jButton3ActionPerformed
 
